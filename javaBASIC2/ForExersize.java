class ForExersize
{
	public static void main(String[] args) 
	{
		
		for(char ch='A';ch<='Z';ch++)
		{	
		System.out.println(ch);
		}
		
		for(char ch='a';ch<='z';ch++)
		{	
		System.out.println(ch);
		}
		
		for(char ch='0';ch<='9';ch++)
		{	
		System.out.println(ch);
		}
	}
}
